#include <iostream>

int main()
{
    // 可以用bitwise 左移或右移的形式进行对2的倍数的乘除操作

    // << bit 整体左移 (6 << 2) 就等于 0110 整体左移两位,空位用0补齐
    // 就等于 0001 1000 == 24. 相当于乘以2的平方.
    int a = 6;
    std::cout << (a << 2) << '\n';

    // 反之 >> 相当于整体右移. 如 (240 >> 4) 等于 1111 0000 整体向右移动四位.
    // 空出来的位用零补齐. 等于 0000 1111 等于 1111 也就是15.
    // 相当于 240 / 2^4
    int b = 240;
    std::cout << (b >> 4) << '\n';

    return 0;
}